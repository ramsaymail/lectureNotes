// assignment operator
let r = 5;

// addition assignment operator
r += 1 // r = r + 1
console.log(r);

// subraction assignment operator
r  -= 1;
console.log(r);

// multiplication assignment operator
r *= 1;
console.log(2);

// division assigment operator
r /= 2;
console.log(r);

// remainder (modulus) assigment operator
r %= 2;
console.log(r);

// exponential assigment operator
r **= 1 // r = r ** 1
console.log(r);